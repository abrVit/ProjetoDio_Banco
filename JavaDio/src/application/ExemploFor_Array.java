package application;

public class ExemploFor_Array {

    public static void main(String[] args) {
        
        String alunos[] = {"FELIPE", "JONAS", "JULIA", "MARCOS"};

        System.out.println(alunos.length);

        for (int i = 0; i < alunos.length; i++){
            System.out.println(alunos[i]);
        }

    }

}
